package com.cafe24.dmsthch.Material;

public class BoardData {
	
	private String licenseKindergarten; 
	private int teacherNo; 
	private int categoryNo;
	private String dataOriginalName; 
	private String dataStorageName; 
	private String dataUrl;
	private int dataSize;
	private String dataDay;
	
	
	
	public String getLicenseKindergarten() {
		return licenseKindergarten;
	}
	public void setLicenseKindergarten(String licenseKindergarten) {
		this.licenseKindergarten = licenseKindergarten;
	}
	public int getTeacherNo() {
		return teacherNo;
	}
	public void setTeacherNo(int teacherNo) {
		this.teacherNo = teacherNo;
	}
	public int getCategoryNo() {
		return categoryNo;
	}
	public void setCategoryNo(int categoryNo) {
		this.categoryNo = categoryNo;
	}
	public String getDataOriginalName() {
		return dataOriginalName;
	}
	public void setDataOriginalName(String dataOriginalName) {
		this.dataOriginalName = dataOriginalName;
	}
	public String getDataStorageName() {
		return dataStorageName;
	}
	public void setDataStorageName(String dataStorageName) {
		this.dataStorageName = dataStorageName;
	}
	public String getDataUrl() {
		return dataUrl;
	}
	public void setDataUrl(String dataUrl) {
		this.dataUrl = dataUrl;
	}
	public int getDataSize() {
		return dataSize;
	}
	public void setDataSize(int dataSize) {
		this.dataSize = dataSize;
	}
	public String getDataDay() {
		return dataDay;
	}
	public void setDataDay(String dataDay) {
		this.dataDay = dataDay;
	}
	
	
}
