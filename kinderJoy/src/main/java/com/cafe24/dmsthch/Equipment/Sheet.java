package com.cafe24.dmsthch.Equipment;

public class Sheet {
	private int sheetNo;
	private int teacherNo;
	private String licenseKindergarten;
	private String sheetContent;
	public int getSheetNo() {
		return sheetNo;
	}
	public void setSheetNo(int sheetNo) {
		this.sheetNo = sheetNo;
	}
	public int getTeacherNo() {
		return teacherNo;
	}
	public void setTeacherNo(int teacherNo) {
		this.teacherNo = teacherNo;
	}
	public String getLicenseKindergarten() {
		return licenseKindergarten;
	}
	public void setLicenseKindergarten(String licenseKindergarten) {
		this.licenseKindergarten = licenseKindergarten;
	}
	public String getSheetContent() {
		return sheetContent;
	}
	public void setSheetContent(String sheetContent) {
		this.sheetContent = sheetContent;
	}
	
}
