package com.cafe24.dmsthch.Equipment;

public class EquipmentRequest {
	private String requestNo;
	private String licenseKindergarten;
	private String requestName;
	private String categoryNo;
	private String requestAmount;
	private String teacherNo;
	private String requestDay;
	private String requestReason;
	private String requestPrice;
	private String requestStant;
	public String getRequestNo() {
		return requestNo;
	}
	public void setRequestNo(String requestNo) {
		this.requestNo = requestNo;
	}
	public String getLicenseKindergarten() {
		return licenseKindergarten;
	}
	public void setLicenseKindergarten(String licenseKindergarten) {
		this.licenseKindergarten = licenseKindergarten;
	}
	public String getRequestName() {
		return requestName;
	}
	public void setRequestName(String requestName) {
		this.requestName = requestName;
	}
	public String getCategoryNo() {
		return categoryNo;
	}
	public void setCategoryNo(String categoryNo) {
		this.categoryNo = categoryNo;
	}
	public String getRequestAmount() {
		return requestAmount;
	}
	public void setRequestAmount(String requestAmount) {
		this.requestAmount = requestAmount;
	}
	public String getTeacherNo() {
		return teacherNo;
	}
	public void setTeacherNo(String teacherNo) {
		this.teacherNo = teacherNo;
	}
	public String getRequestDay() {
		return requestDay;
	}
	public void setRequestDay(String requestDay) {
		this.requestDay = requestDay;
	}
	public String getRequestReason() {
		return requestReason;
	}
	public void setRequestReason(String requestReason) {
		this.requestReason = requestReason;
	}
	public String getRequestPrice() {
		return requestPrice;
	}
	public void setRequestPrice(String requestPrice) {
		this.requestPrice = requestPrice;
	}
	public String getRequestStant() {
		return requestStant;
	}
	public void setRequestStant(String requestStant) {
		this.requestStant = requestStant;
	}
	
}
