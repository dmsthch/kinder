package com.cafe24.dmsthch.Child;

public class ChildDevelopment {
	private String developmentNo;
	private String teacherFormationNo;
	private String kidFormationNo;
	private String developmentYear;
	public String getDevelopmentNo() {
		return developmentNo;
	}
	public void setDevelopmentNo(String developmentNo) {
		this.developmentNo = developmentNo;
	}
	public String getTeacherFormationNo() {
		return teacherFormationNo;
	}
	public void setTeacherFormationNo(String teacherFormationNo) {
		this.teacherFormationNo = teacherFormationNo;
	}
	public String getKidFormationNo() {
		return kidFormationNo;
	}
	public void setKidFormationNo(String kidFormationNo) {
		this.kidFormationNo = kidFormationNo;
	}
	public String getDevelopmentYear() {
		return developmentYear;
	}
	public void setDevelopmentYear(String developmentYear) {
		this.developmentYear = developmentYear;
	}
	
	
}
