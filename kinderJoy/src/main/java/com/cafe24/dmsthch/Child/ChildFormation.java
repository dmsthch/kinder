package com.cafe24.dmsthch.Child;

public class ChildFormation {
	private String kidFormationNo;
	private String licenseKindergarten;
	private String kidNo;
	private String classNo;
	
	public String getKidFormationNo() {
		return kidFormationNo;
	}
	public void setKidFormationNo(String kidFormationNo) {
		this.kidFormationNo = kidFormationNo;
	}
	public String getLicenseKindergarten() {
		return licenseKindergarten;
	}
	public void setLicenseKindergarten(String licenseKindergarten) {
		this.licenseKindergarten = licenseKindergarten;
	}
	public String getKidNo() {
		return kidNo;
	}
	public void setKidNo(String kidNo) {
		this.kidNo = kidNo;
	}
	public String getClassNo() {
		return classNo;
	}
	public void setClassNo(String classNo) {
		this.classNo = classNo;
	}
	
	
} 
