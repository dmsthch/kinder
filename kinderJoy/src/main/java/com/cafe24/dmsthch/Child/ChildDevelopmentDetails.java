package com.cafe24.dmsthch.Child;

public class ChildDevelopmentDetails {
	
	private String detailesNo;
	private String developmentNo;
	private String licenseKindergarten;
	private String developmentGroup;
	private String opserveNo;
	private String developmentTitle;
	private String developmentCase;
	private String developmentDay;
	
	public String getDetailesNo() {
		return detailesNo;
	}
	public void setDetailesNo(String detailesNo) {
		this.detailesNo = detailesNo;
	}
	public String getDevelopmentNo() {
		return developmentNo;
	}
	public void setDevelopmentNo(String developmentNo) {
		this.developmentNo = developmentNo;
	}
	public String getLicenseKindergarten() {
		return licenseKindergarten;
	}
	public void setLicenseKindergarten(String licenseKindergarten) {
		this.licenseKindergarten = licenseKindergarten;
	}
	public String getDevelopmentGroup() {
		return developmentGroup;
	}
	public void setDevelopmentGroup(String developmentGroup) {
		this.developmentGroup = developmentGroup;
	}
	public String getOpserveNo() {
		return opserveNo;
	}
	public void setOpserveNo(String opserveNo) {
		this.opserveNo = opserveNo;
	}
	public String getDevelopmentTitle() {
		return developmentTitle;
	}
	public void setDevelopmentTitle(String developmentTitle) {
		this.developmentTitle = developmentTitle;
	}
	public String getDevelopmentCase() {
		return developmentCase;
	}
	public void setDevelopmentCase(String developmentCase) {
		this.developmentCase = developmentCase;
	}
	public String getDevelopmentDay() {
		return developmentDay;
	}
	public void setDevelopmentDay(String developmentDay) {
		this.developmentDay = developmentDay;
	}
	
}
