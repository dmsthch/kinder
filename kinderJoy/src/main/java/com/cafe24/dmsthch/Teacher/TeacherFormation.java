package com.cafe24.dmsthch.Teacher;

public class TeacherFormation {
	private int teacherFormationNo;
	private int teacherNo;
	private int classNo;
	private String licenseKindergarten;
	
	public int getTeacherFormationNo() {
		return teacherFormationNo;
	}
	public void setTeacherFormationNo(int teacherFormationNo) {
		this.teacherFormationNo = teacherFormationNo;
	}
	public int getTeacherNo() {
		return teacherNo;
	}
	public void setTeacherNo(int teacherNo) {
		this.teacherNo = teacherNo;
	}
	public int getClassNo() {
		return classNo;
	}
	public void setClassNo(int classNo) {
		this.classNo = classNo;
	}
	public String getLicenseKindergarten() {
		return licenseKindergarten;
	}
	public void setLicenseKindergarten(String licenseKindergarten) {
		this.licenseKindergarten = licenseKindergarten;
	}
}
