public class EducationForm {
	private int formNo;
	private int formOrder;
	private String licenseKindergarten;
	private String formVal;
	private String formMerge;
	private String formBorders;
	
	public String getFormBorders() {
		return formBorders;
	}
	public void setFormBorders(String formBorders) {
		this.formBorders = formBorders;
	}
	public String getFormMerge() {
		return formMerge;
	}
	public void setFormMerge(String formMerge) {
		this.formMerge = formMerge;
	}
	public int getFormNo() {
		return formNo;
	}
	public void setFormNo(int formNo) {
		this.formNo = formNo;
	}
	public int getFormOrder() {
		return formOrder;
	}
	public void setFormOrder(int formOrder) {
		this.formOrder = formOrder;
	}
	public String getLicenseKindergarten() {
		return licenseKindergarten;
	}
	public void setLicenseKindergarten(String licenseKindergarten) {
		this.licenseKindergarten = licenseKindergarten;
	}
	public String getFormVal() {
		return formVal;
	}
	public void setFormVal(String formVal) {
		this.formVal = formVal;
	}
	
	
	
}
